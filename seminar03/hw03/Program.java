// В моем случае сообщения об ошибках выбрасывает класс со спецификацией математических операций, а класс 
// MiniCalculator выводит их в консоль без остановки программы и сохраняет их в бэклог

public class Program {
    public static void main(String[] args) {

        MiniCalculator mc = new MiniCalculator();

        System.out.println("Live view:");
        mc.add(3,7);
        mc.substract(3,7);
        mc.multiply(4,4);
        mc.divide(5,2);
        mc.divide(5,0); // выведет и запишет сообщение об ошибке
        mc.power(2,4); 
        mc.power(5,-10); // выведет и запишет сообщение об ошибке

        System.out.println("Backlog:"); // покажет содержимое бэклога (повторит все что было выше)
        mc.printBackLog();
    }
}
